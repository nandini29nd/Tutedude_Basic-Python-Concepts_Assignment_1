# Python program to create a personalized greeting

# Taking first name and last name as input from the user
first_name = input("Enter your first name: ")
last_name = input("Enter your last name: ")

# Concatenating full name
full_name = first_name + " " + last_name

# Printing personalized greeting message
print(f"\nHello, {full_name}! Welcome to the Python program.")
